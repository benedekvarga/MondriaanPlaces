import UIKit

extension UIColor {
    // MARK: - Gray

    public static var mdcLightGray: UIColor {
        return UIColor(hex: "#D9D9DB")
    }

    public static var mdcGray: UIColor {
        return UIColor(hex: "#9F9DA0")
    }

    public static var mdcDarkGray: UIColor {
        return UIColor(hex: "#5F5F61")
    }

    // MARK: - Purple

    public static var mdcPurple: UIColor {
        return UIColor(hex: "#7C1BE7")
    }

    // MARK: - Yellow

    public static var mdcYellow: UIColor {
        return UIColor(hex: "#E4DF14")
    }
}
